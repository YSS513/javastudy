package kr.co.ezen;

public class MainEx {
	public static void main(String[] args) {
		String msg = "what";
		String msg2 = "hi";
		String msg3 = "good";
		String msg4 = "morning";

		// 변수종류 + 변수 = 상수
		// 스트링은 문자열을 뜻한다.
		// 인트는 숫자열을 뜻한다.

		// 변수 선언 공식
		// 자료형 변수명 = 자료형에 맞는 값;

		System.out.println(msg);
		System.out.println(msg2);
		System.out.println(msg3);
		System.out.println(msg4);
		System.out.println(msg);
		System.out.println(msg3);
		System.out.println(msg2);
		System.out.println(msg);

		// String msg = "hello"; 익숙해지기
		// int a = n; 익숙해지기

		// 기본적인 8가지 기본자료형 사용법
		boolean a = true;
		boolean b = false;
		char c = '가';
		byte d = 1;
		short e = 2;
		int f = 3;
		long g = 2500000000L;
		float h = 1.5F;
		double i = 3.14;
	}

}
